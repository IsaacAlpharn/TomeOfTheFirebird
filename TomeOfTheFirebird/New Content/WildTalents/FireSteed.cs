namespace TomeOfTheFirebird.New_Content.WildTalents
{
    class FireSteed
    {
        /*
          Fire Steed

Source PPC:WO

Element fire; Type utility (Sp); Level 2; Burn 0

You imbue a willing animal, magical beast, or other creature that can serve as a suitable mount with elemental power from the Plane of Fire, wreathing it in flames for 1 round.

You can accept 1 point of burn to increase the duration to 1 round per kineticist level you have. The mount gains fire resistance 10 and its speed increases by 10 feet. Creatures other than you who touch the mount, attack the mount with a natural weapon or unarmed strike, or attempt to mount the creature take 1d6 points of fire damage every round they are in contact with the mount.
         */

        /*
         * Improved Fire Steed

Source PPC:WO

Element fire; Type utility (Sp); Level 5; Burn 0
Prerequisite fire steed

When using fire steed, the mount’s fire resistance increases to 20 and its speed increases by an additional 10 feet. The mount can move over lava without difficulty or a decrease in speed.
         */


        /*
         * Greater Fire Steed

Source PPC:WO

Element fire; Type utility (Sp); Level 8; Burn 0
Prerequisite fire steed, improved fire steed

When using fire steed, the mount gains immunity to fire and its speed increases by an additional 10 feet. In addition, when the mount takes a run action, it can run up steep slopes at its normal speed and down steep slopes without its rider needing to attempt a Ride check.
         */

    }
}
